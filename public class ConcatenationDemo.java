public class a {
    public static void main(String[] args) {
        // Long line broken into multiple concatenated strings
        String longLine = "This is a long line that needs to be broken into multiple parts " +
                          "to prevent it from being too long.";

        System.out.println(longLine);

        // Another example with concatenation
        int num1 = 10;
        int num2 = 20;
        int sum = num1 + num2;
        String message = "The sum of " + num1 + " and " + num2 + " is " + sum + ".";
        System.out.println(message);
    }
}
